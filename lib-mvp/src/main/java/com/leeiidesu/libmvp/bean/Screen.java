package com.leeiidesu.libmvp.bean;

/**
 * 屏幕 Created by Leeii on 2015/9/17.
 */
public class Screen {
    
    /**
     * 构造方法
     * 
     * @param width
     *            屏幕宽
     * @param height
     *            屏幕高
     */
    public Screen(int width, int height) {
        this.width = width;
        this.height = height;
    }
    
    /** 宽 */
    public int width;
    
    /** 高 */
    public int height;
    
}
